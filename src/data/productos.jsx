// Función que simula traer todos los productos con una demora
export const getProducts = () => {
  return new Promise((resolve, reject) => {
    const error = false; // acá podés simular un error si querés

    setTimeout(() => {
      if (error) {
        // si hay error, rechazo la promesa con mensaje
        reject("No pudimos cargar los productos. Intentalo de nuevo más tarde.");
      } else {
        // si todo va bien, resuelvo con el array completo de productos
        resolve(productos);
      }
    }, 1500); // demora simulada de 1.5 segundos
  });
};

// Función que simula traer un solo producto por su id
export const getOneProduct = (id) => {
  return new Promise((resolve, reject) => {
    setTimeout(() => {
      // busco el producto cuyo id coincida (transformo id a número para asegurar)
      const productoEncontrado = productos.find((p) => p.id === Number(id));
      if (productoEncontrado) {
        // si lo encuentro, resuelvo con ese producto
        resolve(productoEncontrado);
      } else {
        // si no lo encuentro, rechazo con mensaje de error
        reject("Producto no encontrado.");
      }
    }, 1500);
  });
};
